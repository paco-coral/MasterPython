#Numeros a Calcular
a = 5
b = 2
#Operador Aritmetico Suma
suma = a + b
print ("Suma : ", suma)

#Operador Aritmetico Resta
resta = a - b
print ("Resta : ", resta)

#Operador Aritmetico Multiplicaci√≥n
multi = a * b
print ("Multiplicacion : ", multi)

#Operador Aritmetico Divicion
division = a / b
print ("Division : ", division)

#Operador Aritmetico Modulo
modulo = a % b
print ("Modulo :", modulo)

#Operador Aritmetico Exponente
exponente = a ** b
print ("Exponente : ", exponente)